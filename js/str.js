var s="JavaScript is a powerful and popular programming language"

console.log(s.toUpperCase())  // Converting the complete string into Uppercase letters

console.log(s.toLowerCase())  // Converting the complete string into lowercase letters

console.log(s.indexOf("popular"))   // Finding the index of the string Popular

console.log(s.slice(16,24))   // Displaying the word powerful by using slice method

console.log(s.replace("JavaScript","JS"))   // Replacing the word "JavaScript" with the word "JS"

console.log(s.includes("language"))  // Checking wheather the word "language" is the in the string or not